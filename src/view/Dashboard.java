    public void fillClassesCombo(){
     
        ClassesDAO cl = new ClassesDAO();
        ResultSet rs = cl.getAvailableClasses();
        try{
            while (rs.next()) {
                ClassBox.addItem(rs.getString(2));
                punishClassCombo.addItem(rs.getString(2));
            }
        }catch(Exception e){
               e.printStackTrace();
        }
        
    }
    
    public void fillStatusCombo() {

        StatusDAO st = new StatusDAO();
        ResultSet rs = st.getStatuses();
        try {
            while (rs.next()) {
                statusBox.addItem(rs.getString(2));
                pStatusBox.addItem(rs.getString(2));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
    
    
