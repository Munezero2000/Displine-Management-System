    private void punishExecuteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_punishExecuteBtnActionPerformed
        String feedback = "";
        try{
        Punishment punishment = new Punishment(pIdBox.getText().trim(), reasonBox.getText().trim(), Integer.parseInt(punishDaysBox.getText().trim()), Integer.parseInt(marksDeduBox.getText().trim()), SessionManagement.id, pStatusBox.getSelectedIndex()+1);
        PunishmentDAO pn = new PunishmentDAO();
        
        int result = JOptionPane.showConfirmDialog(this, "Are you sure you want execute this?", "Confirmation", JOptionPane.YES_NO_OPTION);
        
        if (result == JOptionPane.YES_OPTION) {
            feedback = feedback = pn.AddStudentPunishment(punishment);
            // Reduce Student marks and  update the status
            Student std = new Student();
            std.setStudentId(pIdBox.getText());
            StudentDAO Stdao = new StudentDAO();
            Student Holder = new Student();
            Holder = Stdao.getStudentById(std);
            if(Holder != null){
                int newStudentMarks = Holder.getMarks() - Integer.parseInt(marksDeduBox.getText());
                std = new Student();
                std.setMarks(newStudentMarks);
                std.setStatus(pStatusBox.getSelectedIndex()+1);
                std.setStudentId(pIdBox.getText().trim());
                Stdao.updateStudentMarksAndStatus(std);
            }
            JOptionPane.showMessageDialog(this, feedback);
            pIdBox.setEditable(true);
        } else {
            return;
        } } catch(Exception e)   {
            e.printStackTrace();
            pIdBox.setEditable(true); 
        }    
    }//GEN-LAST:event_punishExecuteBtnActionPerformed

